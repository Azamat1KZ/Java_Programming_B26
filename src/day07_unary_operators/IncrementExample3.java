package day07_unary_operators;

public class IncrementExample3 {
    public static void main(String[] args) {

        int a = 10; //
        int b = a + 1; //
        System.out.println(a);
        System.out.println(b);

        int c = ++a; // 11
        System.out.println(c);
        System.out.println(a);

    }
}
