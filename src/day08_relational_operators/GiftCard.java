package day08_relational_operators;

public class GiftCard {

    public static void main(String[] args) {

        double giftCard = 200;
        double item1Price = 165.5, item2Price = 30.4;

        giftCard -= item1Price;

        System.out.println(giftCard);

        giftCard -= item2Price;

        System.out.println(giftCard);




    }

}
