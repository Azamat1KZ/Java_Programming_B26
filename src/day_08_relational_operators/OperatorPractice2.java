package day_08_relational_operators;

public class OperatorPractice2 {
    public static void main(String[] args) {


        int a = 40; // a= 40 -> 39
        System.out.println(--a); // pre decrement, subtract 1, right away, then it prints the value of a, which is 39
        System.out.println(--a); // pre decrement, subtract 1 right away, which is 38
        System.out.println(a--); // post decrement, first
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);


    }
}
